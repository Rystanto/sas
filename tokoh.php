<h2><center>Tokoh-Tokoh Penting Dalam Sumpah Pemuda</center></h2>
<p><center>Soegondo Djojopoespito (PPPI)
Soegondo Djojopoespito adalah ketua yang memimpin serangkaian kegiatan selama Kongres Pemuda II berlangsung. Ia merupakan seorang aktivis organisasi dari PPPI atau dalam bahasa Belanda disebut Indonesische Studentbond.</center></p>
<p><center>R. M. Joko Marsaid (Jong Java)
Selama Kongres Pemuda II, tugas Soegondo dibantu oleh R.M. Joko Marsaid yang berperan sebagai wakil ketua. Pemilik nama lain Tirtodiningrat ini merupakan sosok pemuda pandai yang tergabung dalam kelompok Jong Java.</center></p>
<p><center>Mohammad Yamin (Jong Sumatranen Bond)
Mohammad Yamin dalam sejarah Sumpah Pemuda, berperan sebagai penggagas, dan sekretaris Kongres Pemuda II. Ia turut merancang teks Sumpah Pemuda dan mendorong penggunaan bahasa Indonesia sebagai bahasa persatuan.
Pahlawan Nasional ini juga merupakan seorang sastrawan, sejarawan, budayawan, sekaligus politikus. Ia juga ahli hukum yang sangat dihormati di Indonesia.</center></p>
<p><center> Amir Sjarifoeddin (Jong Bataks Bond)
Amir Sjarifoeddin Harahap merupakan bendahara dalam Kongres Pemuda II. Ia juga ikut serta dalam mencetuskan ide-ide selama perumusan naskah Sumpah Pemuda.
Ia merupakan pemuda yang masuk ke jajaran Kongres Pemuda II. Ia merupakan perwakilan Jong Bataks Bond dalam kongres tersebut.</center></p>
<p><center>Johan Mohammad Cai (Jong Islamieten Bond)
Johan Mohammad Cai berperan sebagai Pembantu I dalam susunan kepanitiaan pelaksana Kongres Pemuda II. Latar belakangnya adalah peranakan Tionghoa dan tergabung dalam Jong Islamieten Bond.</center></p>
<p><center>R. Katja Soengkana (Pemuda Indonesia)
R. Katja Soengkana berperan sebagai Pembantu II dalam susunan kepanitiaan pelaksana Kongres Pemuda II. Sosoknya dalam Kongres Pemuda II yaitu sebagai perwakilan dari kelompok Pemuda Indonesia atau Jong Indonesie.</center></p>
<p><center> Rumondor Cornelis Lefrand Senduk (Jong Celebes)
Rumondor Cornelis Lefrand Senduk berperan sebagai Pembantu III dalam susunan kepanitiaan pelaksana Kongres Pemuda II. Ia merupakan seorang dokter dan politikus Indonesia asal Minahasa, Sulawesi Utara.</center></p>
<p><center>Johannes Leimena (Jong Ambon)
Johannes Leimena berperan sebagai Pembantu IV dalam susunan kepanitiaan pelaksana Kongres Pemuda II. Latar belakang Leimena merupakan pemuda dari Jong Ambon yang berprofesi sebagai dokter dan politisi Indonesia.</center></p>
<p><center> Mohammad Rochjani Su'ud (Pemuda Kaum Betawi)
Mohammad Rochjani Su'ud berperan sebagai Pembantu V dalam susunan kepanitiaan pelaksana Kongres Pemuda II. Latar belakangnya yaitu seorang ahli hukum. 
Ia menjadi perwakilan Pemuda Kaum Betawi dalam Kongres Pemuda II. Sosoknya juga tidak akan dilupakan dalam sejarah kongres ini.</center></p>
<p><center>W. R. Soepratman (Pencipta lagu Indonesia Raya)
Wage Rudolf Soepratman atau W.R. Soepratman adalah seorang wartawan, violinis, sekaligus komponis asal Indonesia. Beliau dipilih sebagai pengisi acara dan menciptakan lagu kebangsaan 'Indonesia Raya'.
Ia juga memperdengarkan lagu tersebut di hadapan peserta Kongres Pemuda II. Ketika itu ia memperdengarkan lagu 'Indonesia Raya' tanpa teks, dengan biola untuk pertama kalinya.</center></p>
